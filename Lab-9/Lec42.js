// Зберігаємо поточний час на початку
let start = new Date();

// Тривалість таймера у мілісекундах (10 секунд = 10000 мс)
let duration = 10000;

console.log("Таймер запущено");

// Виконуємо цикл, поки не пройде 10 секунд
while (new Date() - start < duration) {
    // Порожній цикл — очікуємо, поки пройде 10 секунд
}

console.log("10 секунд пройшло");

