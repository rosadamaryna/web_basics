let x = 5;

// Унарний плюс (+) — перетворює рядок у число
let a = +"42"; // 42

// Унарний мінус (-) — змінює знак
let b = -x; // -5

// Оператор інкременту (++) — збільшує значення на 1
x++; // 6

// Оператор декременту (--) — зменшує значення на 1
x--; // 5

// Логічне заперечення (!) — повертає протилежне булеве значення
let c = !true; // false

// Бітове НЕ (~) — інвертує всі біти числа
let d = ~x; // -(x + 1), тобто -6

// typeof — визначає тип змінної
console.log(typeof x); // "number"

// delete — видаляє властивість об'єкта
let obj = { name: "Alice" };
delete obj.name; // obj = {}

// void — повертає undefined
let e = void 0; // undefined
